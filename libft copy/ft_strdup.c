#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dst;
	size_t	len;

	len = ft_strlen(s1) + 1;
	dst = malloc(len);
	if (dst == NULL)
		return (dst);
	ft_strlcpy(dst, s1, len);
	return (dst);
}
